import { useState } from 'react'

function AddToCartButton({ id, cart, setCart, title, thumbnail, price }) {
  const onCart = cart.find((game) => game.id === id)

  const handleClick = () => {
    setCart([...cart, { id, title, thumbnail, price }])
  }
  if (onCart) {
    return (
      <div>
        <button className="bg-green-200 h-16 w-16 rounded-2xl flex text-center text-sm items-center justify-center opacity-40 cursor-not-allowed max-xl:h-14 max-xl:w-14">
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-6 h-6"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M2.25 3h1.386c.51 0 .955.343 1.087.835l.383 1.437M7.5 14.25a3 3 0 00-3 3h15.75m-12.75-3h11.218c1.121-2.3 2.1-4.684 2.924-7.138a60.114 60.114 0 00-16.536-1.84M7.5 14.25L5.106 5.272M6 20.25a.75.75 0 11-1.5 0 .75.75 0 011.5 0zm12.75 0a.75.75 0 11-1.5 0 .75.75 0 011.5 0z"
            />
          </svg>
        </button>
      </div>
    )
  } else {
    return (
      <div>
        <button
          onClick={() => handleClick({ id })}
          className="bg-gray-200 h-16 w-16 rounded-2xl flex text-center text-sm items-center justify-center hover:bg-gray-300 max-xl:h-14 max-xl:w-14"
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth="1.5"
            stroke="currentColor"
            className="w-6 h-6"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M2.25 3h1.386c.51 0 .955.343 1.087.835l.383 1.437M7.5 14.25a3 3 0 00-3 3h15.75m-12.75-3h11.218c1.121-2.3 2.1-4.684 2.924-7.138a60.114 60.114 0 00-16.536-1.84M7.5 14.25L5.106 5.272M6 20.25a.75.75 0 11-1.5 0 .75.75 0 011.5 0zm12.75 0a.75.75 0 11-1.5 0 .75.75 0 011.5 0z"
            />
          </svg>
        </button>
      </div>
    )
  }
}

export default AddToCartButton
